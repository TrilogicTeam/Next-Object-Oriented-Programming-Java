    private void tb_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tb_hapusActionPerformed
        int row = jTable1.getSelectedRow(); //get Data jtable1 in selected
        if(row >= 0){
            int ok = JOptionPane.showConfirmDialog(null, "Delete this row?", "Confirm",
                    JOptionPane.YES_NO_OPTION);
            if (ok == 0){ //confirm if dialog delete is "yes"
                mdl.removeRow(row);
            }
            jTable1.setModel(mdl); //refresh data jtable1
        }
    }//GEN-LAST:event_tb_hapusActionPerformed

