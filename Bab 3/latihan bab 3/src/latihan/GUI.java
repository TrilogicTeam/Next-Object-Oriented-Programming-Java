    private void tb_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tb_simpanActionPerformed
        StringBuffer alldata = new StringBuffer();
        for (int a = 0; a < jTable1.getRowCount(); a++){
            for(int b = 0; b < jTable1.getColumnCount();b++){
                alldata.append(jTable1.getValueAt(a, b)).append("\t");
            }
            alldata.append(System.getProperty("line.separator"));
        }
        File file = new File("C:\\Users\\MasAS\\Documents\\NetBeansProjects\\latihan bab 3\\simpan.txt");
        try {
            BufferedWriter cetak = new BufferedWriter(new FileWriter(file));
            cetak.write(alldata.toString());
            cetak.newLine();
            cetak.flush();
            cetak.close();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_tb_simpanActionPerformed

    
